/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  if ((__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) != RESET))
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart1);
    if (Slave1_Object)
    {
      /*Stop DMA transmission to prevent busy receiving data and interference during data processing*/
      HAL_UART_DMAStop(&huart1);
      /*Get the number of untransmitted data in DMA*/
      mdRTU_Recive_Len(Slave1_Object) = MODBUS_PDU_SIZE_MAX - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
      /*Reopen DMA reception*/
      HAL_UART_Receive_DMA(&huart1, mdRTU_Recive_Buf(Slave1_Object), MODBUS_PDU_SIZE_MAX);
    }
    /*After opening the serial port interrupt, the semaphore has not been created*/
    if (LTE_ReciveHandle != NULL)
    {
      /*Notification task processing*/
      osSemaphoreRelease(LTE_ReciveHandle);
    }
  }
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  if ((__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) != RESET))
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart2);
    HAL_UART_DMAStop(&huart2);
    g_Dwin.RxCount = RX_BUF_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);
    /*Reopen DMA reception*/
    HAL_UART_Receive_DMA(&huart2, g_Dwin.RxBuf, RX_BUF_SIZE);
    /*After opening the serial port interrupt, the semaphore has not been created*/
    if (Dwin_ReciveHandle != NULL)
    {
      /*Notification task processing*/
      osSemaphoreRelease(Dwin_ReciveHandle);
    }
  }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

